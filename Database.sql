CREATE DATABASE IF NOT EXISTS restaurant_orders;
USE restaurant_orders;
-- 1. TABEL MASTER
CREATE TABLE Customers (
    customer_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_name VARCHAR(255) NOT NULL,
    email VARCHAR(255),
    phone VARCHAR(20)
);

-- 2. TABEL Caategories
CREATE TABLE Categories (
    category_id INT AUTO_INCREMENT PRIMARY KEY,
    category_name VARCHAR(100) NOT NULL
);

-- 3. TABEL Payment Methods
CREATE TABLE Payment_Methods (
    payment_id INT AUTO_INCREMENT PRIMARY KEY,
    method_name VARCHAR(100) NOT NULL
);

-- 4. TABEL Tables
CREATE TABLE Tables (
    table_id INT AUTO_INCREMENT PRIMARY KEY,
    table_number VARCHAR(10) NOT NULL,
    capacity INT NOT NULL DEFAULT 4,
    location VARCHAR(50),
    status ENUM('Available', 'Occupied', 'Reserved', 'Maintenance') DEFAULT 'Available'
);

-- 5. TABEL MENU
CREATE TABLE Menu (
    menu_id INT AUTO_INCREMENT PRIMARY KEY,
    item_name VARCHAR(255) NOT NULL,
    category_id INT NOT NULL,
    description VARCHAR(500),
    unit_price DECIMAL(10, 2) NOT NULL,
    menu_available BOOLEAN DEFAULT TRUE,
    member_only BOOLEAN DEFAULT FALSE,
    image VARCHAR(500),
    FOREIGN KEY (category_id) REFERENCES Categories(category_id)
);

-- 6. TABEL ORDERS
CREATE TABLE Orders (
    order_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT,
    guest_name VARCHAR(255),
    service_type ENUM('Dine In', 'Take Away') DEFAULT 'Dine In',
    table_id INT,
    payment_id INT,
    order_status ENUM('Pending', 'Completed', 'Cancelled') DEFAULT 'Pending',
    order_time DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id),
    FOREIGN KEY (table_id) REFERENCES Tables(table_id),
    FOREIGN KEY (payment_id) REFERENCES Payment_Methods(payment_id)
);

-- 7. TABEL ORDER DETAILS
CREATE TABLE Order_Details (
    order_detail_id INT AUTO_INCREMENT PRIMARY KEY,
    order_id INT NOT NULL,
    menu_id INT NOT NULL,
    quantity INT NOT NULL,
    total_price DECIMAL(10, 2) NOT NULL,
    request_note VARCHAR(255),
    FOREIGN KEY (order_id) REFERENCES Orders(order_id),
    FOREIGN KEY (menu_id) REFERENCES Menu(menu_id)
);

-- 8. TABEL RESERVATIONS (Member Only)
CREATE TABLE Reservations (
    reservation_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT NOT NULL,
    table_id INT NOT NULL,
    reservation_date DATE NOT NULL,
    check_in TIME NOT NULL,
    check_out TIME NOT NULL,
    party_size INT NOT NULL DEFAULT 2,
    status ENUM('Pending', 'Confirmed', 'Completed', 'Cancelled') DEFAULT 'Pending',
    special_request VARCHAR(500),
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id),
    FOREIGN KEY (table_id) REFERENCES Tables(table_id)
);

-- 9. TABEL REVIEWS
CREATE TABLE Reviews (
    review_id INT AUTO_INCREMENT PRIMARY KEY,
    order_id INT NOT NULL,
    customer_id INT,
    rating INT NOT NULL,
    comment VARCHAR(500),
    review_date DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (order_id) REFERENCES Orders(order_id),
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id)
);

